#-------------- list of symbols and words ---------

# # - allows you to type comments in your code

# """ - allows you to write a string that covers multiple lines of code.

# + - * / - signs used to add, subtract, multiply, and divide

# print() - prints the contents within the parentheses

#  = - assigns a value to a variable

# f"{}" - a refference cell

# .format() - string formatting method in python3, which allows multiple substitutions and value formatting

# end='' - appends space instead of a new line.

# \n - escape sequence used to push anything right of the escape sequence to the next line

# \t - escape sequence used to tab in anything right of the escape sequence

# \\ - escape sequence in order to allow '\' to be printed in a string

# input() - allows user input

# from - refferences the module in which you will import a specific function

# import - the import statement combines two operations; it searches for the named module, then it binds the results of that search to a name in the local scope.

# argv - argument value

# open() - the open() function opens a file, and returns it as a file object.

# .read() - returns the specified number of bytes from the file. Default is -1 which means the whole file.

# .truncate() - used to truncate the file indicated by the specified path to at most specified length

# .write() - writes a string to the file. There is no return value. Due to buffering, the string may not actually show up in the file until the flush() or close() method is called.

# .close() - closes the opened file

# def - the keyword for defining a function. the function name is followed by parameter(s) in (). The colon: signals the start of the function body, which is marked by indentation. Inside the function body, the return statement determines the value to be returned.

# .readline() - reads one entire line from the file.

# += - A += B is shorthand for A = A + B, where A and B can be numbers, or strings, or tuples, or lists (but both must be of the same type).

# .seek() - sets the file's current position at the offset.

# return - Exits a function and optionally pass an expression back to the caller. If you use a return statement with no arguments, the function will return None.

# exists - the most common way to check for the existence of a file in python3 from the os.path module in the standard library.

# len() - returns the length (the number of items) of an object.

# sys - a module you load by importing into the current namespace so that you can access the functions and anything else defined within the module using the module name.

# os.path - module that is always the path module suitable for the operating system Python is running on, and therefore usable for local paths.
